import java.util.*;
public class increanddecre
{
public static void main(String args[])
{
//post increment
int a=5;
System.out.println(a++);
//pre increment
int b=4;
System.out.println(++b);
//post decrement
int c=6;
System.out.println(c--);
//pre decrement
int d=6;
System.out.println(--d);
}
}